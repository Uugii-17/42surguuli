#include <unistd.h>
int main(int ac, char **av)
{
	if(ac != 4 || av[2][0] == 0 || av[2][1] != '\0' || av[3][0] == 0 || av[3][1] != '\0')
	{
		write(1, "\n", 1);
		return(0);
	}
	char find;
	char repl;
	int i;
	i = 0;
	find = av[2][0];
	repl = av[3][0];
	while(av[1][i] != '\0')
	{
		if(av[1][i] == find)
			av[1][i] = repl;
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return(0);
}
