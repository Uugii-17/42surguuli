#include <unistd.h>
int main(int ac , char **av)
{
	int i;
	int j;
	if(ac == 2)
	{
		i = 0;
		while(av[1][i] != '\0')
		{
			if(av[1][i] >= 65 && av[1][i] <= 90)
			{
				j = 0;
				while (j < av[1][i] - 64)
				{
					write(1, &av[1][i] , 1);
					j++;
				}
			}
			else if(av[1][i] >= 97 && av[1][i] <= 122)
			{
				j = 0;
				while(j < av[1][i] - 98)
				{
					write(1, &av[1][i] ,1);
					j++;
				}
			}
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
